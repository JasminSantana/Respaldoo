private void calculaSubtotal(){
 DefaultTableModel model=(DefaultTableModel)tablaProductos.getModel();
    Vector datos=model.getDataVector();
Double subtotal=0.0;


for(Object row : datos){
Vector arrRow=(Vector)row;
if(arrRow.get(1)!=null){
subtotal+=(Double)arrRow.get(2);
}
}
txtSubtotal.setText(""+subtotal);
txtIVA.setText(""+subtotal*0.16);
txtTotal.setText("Datos"+subtotal);

System.out.println(""+subtotal);
}
    

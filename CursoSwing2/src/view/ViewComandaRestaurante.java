/**
 * Metodo que regresa la clave de un producto
 * @param desc nombre del prodcto
 * @return calve del produto
 */
private Double getKeyCosto(String desc){
String key =null;
Double costo=0.0;

for(Map.Entry<String, String> entry : mapCatalogo.entrySet()){
    if(entry.getValue().equals(desc)){
        key=entry.getKey();
}
   
}
for(Map.Entry<String, Double> entry : mapCatalogoCosto.entrySet()){
if(entry.getKey().equals(key)){
    costo=entry.getValue();
}
}
return costo;
}

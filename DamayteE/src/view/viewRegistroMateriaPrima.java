    private boolean validarCajas() {
        boolean isValido = true;
        mensaje = "";
        
        
        if (txtNombre.getText().trim().isEmpty()) {
            mensaje = mensaje + "El nombre  est치 vac칤o\n";
            isValido = false;
        }else
        if (txtDescripcion.getText().trim().isEmpty()) {
            mensaje = mensaje + "La Descripcion  est치 vac칤a\n";
            isValido = false;
        }else
        if (txtCantidad.getText().trim().isEmpty()) {
            mensaje = mensaje + "La Cantidad est치 vac칤a\n";
            isValido = false;
        }else
        if (cboUnidadMedida.getSelectedItem().equals("UNIDADES DE MEDIDA")) {
            mensaje = mensaje + "La Unidad de Medida esta vacia\n";
            isValido = false;
        }else
        if (cboDiaF.getSelectedItem().equals("Dia")) {
            mensaje = mensaje + "El dia esta vac칤o\n";
            isValido = false;
        }else
        if (cboMesF.getSelectedItem().toString().trim().equals("Mes")) {
            mensaje = mensaje + "El mes esta vacio\n";
            isValido = false;
        }else
        if(cboA침oF.getSelectedItem().equals("A침o")) {
            mensaje = mensaje + "El a침o esta vacio\n";
            isValido = false;
        }
        return isValido;
    }
